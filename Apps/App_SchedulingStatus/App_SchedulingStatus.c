/*-----------------------------------------------------Includes------------------------------------------------------*/
/*********************************************************************************************************************/
#include "App_SchedulingStatus.h"

#include "IfxPort_PinMap.h"
#include "Port/Io/IfxPort_Io.h"

#include "FreeRTOS.h"
#include "task.h"

/*********************************************************************************************************************/
/*------------------------------------------------------Macros-------------------------------------------------------*/
/*********************************************************************************************************************/
#define SCHEDULING_STATUS_TOGGLE_PERIOD_MS       500U
#define SCHEDULING_STATUS_LED                    IfxPort_P00_5

/*********************************************************************************************************************/
/*---------------------------------------------Function Implementations----------------------------------------------*/
/*********************************************************************************************************************/
void SchedulingStatusApp_Init(void)
{
    IfxPort_setPinMode(SCHEDULING_STATUS_LED.port,
                       SCHEDULING_STATUS_LED.pinIndex,
                       IfxPort_Mode_outputPushPullGeneral);
}

void SchedulingStatusApp_Run(void *arg)
{
    (void)arg;

    SchedulingStatusApp_Init();

    while (1)
    {
        IfxPort_setPinState(SCHEDULING_STATUS_LED.port,
                            SCHEDULING_STATUS_LED.pinIndex,
                            IfxPort_State_toggled);

        vTaskDelay(pdMS_TO_TICKS(SCHEDULING_STATUS_TOGGLE_PERIOD_MS));
    }
}
